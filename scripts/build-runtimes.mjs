import { execFileSync } from "node:child_process";
import { access, readdir, readFile, writeFile } from "node:fs/promises";
import { join } from "node:path";
import { pathToFileURL } from "node:url";
import { gzipSync } from "node:zlib";

async function exists(path) {
  try {
    await access(path);
    return true;
  } catch {
    return false;
  }
}

async function loadEsbuildFromNpxLakebed() {
  const npmCache = execFileSync("npm", ["config", "get", "cache"], { encoding: "utf8" }).trim();
  const npxCache = join(npmCache, "_npx");
  const entries = await readdir(npxCache, { withFileTypes: true });

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const modules = join(npxCache, entry.name, "node_modules");
    const lakebedPackage = join(modules, "lakebed", "package.json");
    const esbuildPackage = join(modules, "esbuild", "package.json");
    if (!await exists(lakebedPackage) || !await exists(esbuildPackage)) continue;

    const esbuildMetadata = JSON.parse(await readFile(esbuildPackage, "utf8"));
    const entryPath = join(modules, "esbuild", esbuildMetadata.main ?? "lib/main.js");
    const loaded = await import(pathToFileURL(entryPath).href);
    return { esbuild: loaded.default ?? loaded, nodePaths: [modules] };
  }

  throw new Error("Could not find the esbuild copy bundled with npx lakebed. Run npx lakebed --help once and retry.");
}

async function loadEsbuild() {
  try {
    return { esbuild: await import("esbuild"), nodePaths: [] };
  } catch {
    return loadEsbuildFromNpxLakebed();
  }
}

async function bundle(esbuild, nodePaths, entryPoint, platform) {
  const result = await esbuild.build({
    entryPoints: [entryPoint],
    bundle: true,
    format: "esm",
    platform,
    target: "es2022",
    minify: true,
    write: false,
    jsx: "automatic",
    jsxImportSource: "preact",
    nodePaths,
    logLevel: "silent",
  });
  return result.outputFiles[0].text.trim();
}

async function bundledExport(esbuild, nodePaths, entryPoint, exportName) {
  const result = await esbuild.build({
    entryPoints: [entryPoint],
    bundle: true,
    format: "cjs",
    platform: "node",
    target: "es2022",
    minify: true,
    write: false,
    nodePaths,
    logLevel: "silent",
  });
  const module = { exports: {} };
  const evaluate = new Function("module", "exports", result.outputFiles[0].text);
  evaluate(module, module.exports);
  const value = module.exports[exportName];
  if (typeof value !== "string") throw new Error(`${entryPoint} did not export the ${exportName} string.`);
  return value;
}

function packedModule(comment, exportName, source) {
  const packed = gzipSync(source, { level: 9, mtime: 0 }).toString("base64");
  return `${comment}\nexport const ${exportName} = ${JSON.stringify(packed)};\n`;
}

const { esbuild, nodePaths } = await loadEsbuild();
const interfaceRuntime = await bundle(esbuild, nodePaths, "client/objects.ts", "browser");
const workspaceRuntime = await bundle(esbuild, nodePaths, "client/workspace/runtime-entry.ts", "browser");
const serverRuntime = await bundle(esbuild, nodePaths, "server/workspace-runtime-entry.ts", "neutral");
const theme = await bundledExport(esbuild, nodePaths, "client/theme/index.ts", "objectsTheme");

await writeFile(
  "client/runtime/packed.ts",
  packedModule("// Generated by scripts/build-runtimes.mjs from client/objects.ts.", "runtimeCompressed", interfaceRuntime),
);
await writeFile(
  "client/workspace/runtime-packed.ts",
  packedModule("// Generated by scripts/build-runtimes.mjs from client/workspace/runtime-entry.ts.", "workspaceRuntimeCompressed", workspaceRuntime),
);
await writeFile(
  "server/workspace-runtime.ts",
  `// Generated by scripts/build-runtimes.mjs from server/workspace-runtime-entry.ts.\n${serverRuntime}\n`,
);
await writeFile(
  "client/theme/packed.ts",
  packedModule("// Generated by scripts/build-runtimes.mjs from client/theme/index.ts.", "objectsThemeCompressed", theme),
);

console.log("Rebuilt the interface, theme, browser Workspace, and server Workspace runtimes.");
